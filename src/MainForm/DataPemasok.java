    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        String tombol = btnCari.getText();
        if (tombol.equals("Cari")){
            Object[] Baris = {"Kode Pemasok", "Nama Pemasok", "Alamat", "E-Mail", "No.Telp"};
            tabmode = new DefaultTableModel(null, Baris);
            tblpemasok.setModel(tabmode);
            String sql = "Select * from tb_pemasok where kd_kendaraan like '%" + txtCari.getText() + "%'" +
            "or nm_pemasok like '%" + txtCari.getText() + "%'";
            try {
                Statement stat = konek.koneksiDb().createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                while (hasil.next()) {
                    String kd_pemasok = hasil.getString("kd_pemasok");
                    String nm_pemasok = hasil.getString("nm_pemasok");
                    String alamat = hasil.getString("alamat");
                    String email = hasil.getString("email");
                    String no_telp = hasil.getString("no_telp");
                    String[] data = {kd_pemasok,nm_pemasok,alamat,email,no_telp};
                    tabmode.addRow(data);
                }
            } catch (Exception e) {
            }
            btnCari.setText("Batal");
        }else{
            tblpemasok();
            btnCari.setText("Cari");
            txtCari.setText("");
            tambah.setEnabled(true);
        }
    }//GEN-LAST:event_btnCariActionPerformed

