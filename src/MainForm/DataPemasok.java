    private void autokdpemasok() {
        try {
            Connection c = konek.koneksiDb();
            Statement s = c.createStatement();

           String sql = "SELECT * FROM tb_pemasok ORDER by kd_pemasok desc";
            try (ResultSet r = s.executeQuery(sql)) {
                if (r.next()){
                    String kd = r.getString("kd_pemasok").substring(2);
                    String AN = ""+ (Integer.parseInt(kd) + 1);
                    String Nol = "";
                    
                    switch (AN.length()) {
                        case 1:
                            Nol = "00";
                            break;
                        case 2:
                            Nol = "0";
                            break;
                        case 3:
                            Nol = "";
                            break;
                        default:
                            break;
                    }
                    txkd.setText("SP" + Nol + AN);
                } else {
                    txkd.setText("SP001");  
                }
            }
        } catch (SQLException | NumberFormatException e) {
        }    
    }

