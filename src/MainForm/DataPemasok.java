    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
        String kd_pemasok  = txkd.getText(); 
        String nm_pemasok  = txnama.getText();
        String alamat  = txalamat.getText();
        String email = txemail.getText();
        String no_telp = txtelepon.getText();
        
        if ("EDIT".equals(update.getText())) {
            aktif();
            update.setText("UPDATE");
            hapus.setEnabled(false);
            tambah.setEnabled(false);
        } else {
                int ok = JOptionPane.showConfirmDialog(null,"Apakah Anda Yakin Ubah Data Ini?", "Confirmation",JOptionPane.YES_NO_CANCEL_OPTION);
            try (Statement Statement = (Statement)konek.koneksiDb().createStatement()) {
                    Statement.executeUpdate("Update tb_pemasok SET kd_pemasok='"+ kd_pemasok +"',"
                    + "nm_pemasok ='"+ nm_pemasok +"',alamat ='"+ alamat +"',email='"+email +"',"
                    + "no_telp ='"+ no_telp +"' where kd_pemasok='"+kd_pemasok+"'");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Data Pemasok Gagal Di Update" + e);
                }
            kosong();
            autokdpemasok();
            JOptionPane.showMessageDialog(null, "Data Pemasok berhasil Di Update");
            update.setText("EDIT");
            tblpemasok();
            }
    }//GEN-LAST:event_updateActionPerformed

