    public void tblpemasok(){
        DefaultTableModel tbl=new DefaultTableModel();
        tbl.addColumn("Kode Pemasok");
        tbl.addColumn("Nama Pemasok");
        tbl.addColumn("Alamat");
        tbl.addColumn("E-Mail");
        tbl.addColumn("No.Telp");
        tblpemasok.setModel(tbl);
        try{
            Statement Statement=(Statement)konek.koneksiDb().createStatement();
            ResultSet res = Statement.executeQuery("select * from tb_pemasok");
            while (res.next())
            {
                tbl.addRow(new Object[]{
                    res.getString("kd_pemasok"),
                    res.getString("nm_pemasok"),
                    res.getString("alamat"),
                    res.getString("email"),
                    res.getString("no_telp")
                });
             tblpemasok.setModel(tbl);
            }
        }catch (SQLException e){
            JOptionPane.showMessageDialog(rootPane, "salah");
        }
    }
    
