    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed

        int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin ingin Menghapus Data Ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String sql = "delete from tb_login where id_login='"+txid.getText() + "'";
            try {
                PreparedStatement stat = konek.koneksiDb().prepareStatement(sql);
                stat.executeUpdate();
                kosong();
                JOptionPane.showMessageDialog(null, "Data Akun Berhasil Dihapus");
                autoNumber();
                txid.requestFocus();
                tblakun();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Akun Gagal Di Hapus" + e);
            }
        }
    }//GEN-LAST:event_hapusActionPerformed

