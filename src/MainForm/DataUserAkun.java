    private void tblakun() {
        Object[] Baris = {"ID Login", "Username", "Password", "E-Mail", "Role"};
        tabmode = new DefaultTableModel(null, Baris);
        tblakun.setModel(tabmode);
        String sql = "select * from tb_login";
        try {
            Statement stat = konek.koneksiDb().createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id_login = hasil.getString("id_login");
                String username = hasil.getString("username");
                String password = hasil.getString("password");
                String email = hasil.getString("email");
                String role = hasil.getString("role");
                String[] data = {id_login,username,password,email,role};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }
    
