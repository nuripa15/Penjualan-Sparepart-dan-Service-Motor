    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed

        String id_login = txid.getText();
        String username  = txuser.getText();
        String password = txpassword.getText();
        String email = txemail.getText();
        Object role = cmb_role.getSelectedItem();

        if ("EDIT".equals(update.getText())) {
            aktif();
            update.setText("UPDATE");
            hapus.setEnabled(false);
            tambah.setEnabled(false);
        } else {
            int ok = JOptionPane.showConfirmDialog(null,"Apakah Anda Yakin Ubah Data Ini?", "Confirmation",JOptionPane.YES_NO_CANCEL_OPTION);
            try {
                try (Statement Statement = (Statement)konek.koneksiDb().createStatement()) {
                    Statement.executeUpdate("Update tb_login SET id_login='"+ id_login +"',"
                            + "username ='"+ username +"',password='"+ password +"',"
                            + "email ='"+ email +"',role ='"+ role +"' where id_login='"+id_login+"'");
                }
                kosong();
                autoNumber();
                JOptionPane.showMessageDialog(null, "Data Akun berhasil Di Update");
                update.setText("EDIT");
                tblakun();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Akun Gagal Di Update" + e);
            }
        }
    }//GEN-LAST:event_updateActionPerformed

