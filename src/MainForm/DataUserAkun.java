      private void autoNumber() {
        try {
            String sql = "SELECT * FROM tb_login ORDER BY id_login DESC";
            PreparedStatement stat = konek.koneksiDb().prepareCall(sql);
            try (ResultSet rs = stat.executeQuery(sql)) {
                if (rs.next()) {
                    String kd = rs.getString("id_login").substring(3);
                    String AN =  "" + (Integer.parseInt(kd) + 1);
                    String Nol = "";
                    
                    switch (AN.length()) {
                        case 1:
                            Nol = "000";
                            break;
                        case 2:
                            Nol = "00";
                            break;
                        case 3:
                            Nol = "0";
                            break;
                        case 4:
                            Nol = "";
                            break;
                        default:
                            break;
                    }
                    txid.setText("LG" + Nol + AN);
                } else {
                    txid.setText("LG0001");
                }
            }
        } catch (SQLException | NumberFormatException e) {
        }
      }
    

