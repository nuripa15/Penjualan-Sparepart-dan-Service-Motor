    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        
        SimpleDateFormat dformat=new SimpleDateFormat("yyyy-MM-dd");
        String tombol = tambah.getText();
        Object nm_sparepart  = barangmasuk.getSelectedItem();
        String kd_sparepartmsk = kdbarang.getText();
        String tambahstk = tbstok.getText();
        String tglmasuk = dformat.format(tanggalmasuk.getDate());
        
        if (tombol.equals("TAMBAH")) {
            aktif();
            //kosong();
            tambah.setText("SIMPAN");
            }else {
            JOptionPane.showConfirmDialog(null, "Apakah Data anda sudah benar?", "INFORMASI",JOptionPane.YES_NO_OPTION);
                        try {
                            Connection c = konek.koneksiDb();
                            String sql = "INSERT INTO tb_sparepartmasuk VALUES (?, ?, ?, ?, ?)";
                            PreparedStatement p = c.prepareStatement(sql);
                            p.setString(1, null);
                            p.setString(2, (String) nm_sparepart);
                            p.setString(3, kd_sparepartmsk);
                            p.setString(4, tambahstk);
                            p.setString(5, tglmasuk);
                            p.executeUpdate();
                            p.close();
                            kdbarang.requestFocus();
                            } catch (SQLException e) {
                            System.out.println("Terjadi Error");
                            }finally{
                                String   msg="<html>Nama Sparepart         = " +barangmasuk.getSelectedItem()+" <br>"
                                + "Kode Sparepart        = " +kdbarang.getText()+"<br>"
                                + "Stok              = " +tbstok.getText()+"<br>"
                                + "Tanggal Masuk             = " +tanggalmasuk.getDate()+"<html>";
                                JOptionPane optionPane=new JOptionPane();
                                optionPane.setMessage(msg);
                                optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
                                JDialog dialog=optionPane.createDialog(null, "DATA DISIMPAN");
                                dialog.setVisible(true);
                                new TambahStokSparepart().setVisible(true);
                            this.dispose();
                            tblsparepart();
                            tambah.setText("TAMBAH");
                            non_aktif();
                            }
                            
            }
    }//GEN-LAST:event_tambahActionPerformed

    private void barangmasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barangmasukActionPerformed
        if (barangmasuk.getSelectedItem().equals("- Nama Sparepart -")){
            kdbarang.setText("");
        }else{
            try {
                Connection c = konek.koneksiDb();
                Statement s = c.createStatement();

                String sql = "SELECT kd_sparepart, stk, harga FROM tb_sparepart WHERE nm_sparepart ='" + barangmasuk.getSelectedItem() + "'";
                ResultSet r = s.executeQuery(sql);

                while (r.next()) {
                    kdbarang.setText(r.getString("kd_sparepart"));
                    kdbarang.setForeground(Color.BLACK);
                    stokawal.setText(r.getString("stk"));
                    stokawal.setForeground(Color.BLACK);

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }       // TODO add your handling code here:
    }//GEN-LAST:event_barangmasukActionPerformed

