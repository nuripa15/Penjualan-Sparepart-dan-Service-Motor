    private void barangmasuk() {
          
        try {
            Connection c = konek.koneksiDb();
            Statement s = c.createStatement();

            String sql = "SELECT nm_sparepart FROM tb_sparepart WHERE stk !=''";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                barangmasuk.addItem(r.getString("nm_sparepart"));
            }

            r.last();
            int jumlahdata = r.getRow();
            r.first();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }
     
    public void tblsparepart(){
        DefaultTableModel tbl=new DefaultTableModel();
        tbl.addColumn("Kode Sparepart");
        tbl.addColumn("Nama Sparepart");
        tbl.addColumn("Merk");
        tbl.addColumn("Harga");
        tbl.addColumn("Stock");
        tblsparepart.setModel(tbl);
        try{
            Statement Statement=(Statement)konek.koneksiDb().createStatement();
            ResultSet res = Statement.executeQuery("select * from tb_sparepart");
            while (res.next())
            {
                tbl.addRow(new Object[]{
                    res.getString("kd_sparepart"),
                    res.getString("nm_sparepart"),
                    res.getString("merk"),
                    res.getString("harga"),
                    res.getString("stk")
                });
             tblsparepart.setModel(tbl);
            }
        }catch (SQLException e){
            JOptionPane.showMessageDialog(rootPane, "salah");
        }
    }

